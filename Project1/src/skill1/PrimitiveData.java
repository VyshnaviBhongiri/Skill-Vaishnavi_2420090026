package skill1;
public class PrimitiveData {
	    public static void main(String[] args) {
	        byte age = 25;
	        short currentYear = 2025;
	        int population = 1400000000;
	        long distanceToMoon = 384400000L;
	        float pi = 3.14f;
	        double gravity = 9.80665;
	        char grade = 'A';
	        boolean isJavaFun = true;

	        System.out.println("Age: " + age);
	        System.out.println("Current Year: " + currentYear);
	        System.out.println("Population: " + population);
	        System.out.println("Distance to Moon (in meters): " + distanceToMoon);
	        System.out.println("Value of Pi: " + pi);
	        System.out.println("Acceleration due to gravity: " + gravity);
	        System.out.println("Student Grade: " + grade);
	        System.out.println("Is Java fun to learn? " + isJavaFun);
	    }
	}


